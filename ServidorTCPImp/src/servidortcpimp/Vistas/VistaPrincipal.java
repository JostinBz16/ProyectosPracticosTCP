    private void btnIniciarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnIniciarMouseClicked
        if (btnIniciar.getText().equalsIgnoreCase("iniciar")) {
            int puerto = Integer.parseInt(txt_puntoRed.getText());
            servidor = new ServidorTcp(puerto, this);
            servidor.start();
            btnIniciar.setText("Detener");
            btnIniciar.setBackground(Color.RED);
            btnIniciar.setForeground(Color.WHITE);
            lbl_resultado.setText("Online");
            lbl_resultado.setForeground(Color.GREEN);
        } else {
            servidor.detenerServicio();
            btnIniciar.setText("Iniciar");
            btnIniciar.setBackground(Color.GREEN);
            btnIniciar.setForeground(Color.BLACK);
            lbl_resultado.setText("Off line");
            lbl_resultado.setForeground(Color.red);
        }
    }//GEN-LAST:event_btnIniciarMouseClicked

